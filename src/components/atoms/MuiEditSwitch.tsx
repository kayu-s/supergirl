type Props = {
  prevLabel: string;
  nextLabel: string;
  dispatchEvent: any;
  checked: boolean;
};

export default function MuiEditSwitch({
  prevLabel,
  nextLabel,
  dispatchEvent,
  checked,
}: Props) {
  const [check, setCheck] = useState(checked);
  const dispatch = useDispatch();
  useEffect(() => {
    dispatch(dispatchEvent(check));
  }, [check]);

  return (
    <div>
      <FormGroup>
        <FormControlLabel
          control={<MaterialUISwitch sx={{ m: 1 }} checked={check} />}
          label={check ? nextLabel : prevLabel}
          onChange={(e: any) => {
            setCheck(e.target.checked);
          }}
        />
      </FormGroup>
    </div>
  );
}
